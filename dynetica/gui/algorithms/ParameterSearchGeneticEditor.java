    private void editObjectiveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editObjectiveButtonActionPerformed
        String name = (String) targetListBox.getSelectedValue();
        TargetObjective ts = null;
        for (TargetObjective obj : targetList) {
            if (obj.getSubstance().getName().equals(name)) {
                ts = obj;
                break;
            }
        }
        removeSelectedObjective();
        JFrame jf = new JFrame("Edit Objective Function");
        jf.getContentPane().add(new TargetObjectiveEditor(jf, this, ts));
        jf.pack();
        jf.show();
    }//GEN-LAST:event_editObjectiveButtonActionPerformed

