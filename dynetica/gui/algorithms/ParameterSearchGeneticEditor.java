    private void setupLists() {
        List parameters = system.getParameters();
        for (int i=0; i<parameters.size(); i++) {
            Parameter p = (Parameter) parameters.get(i);
            if (p.getName().equals("Time")) {continue;}
            fixedParameters.addElement(p.getName());
        }
    }
    
    public void addTargetObjective(TargetObjective to) {
        targetList.add(to);
        targetObjectives.addElement(to.getSubstance().getName());
    }
    
    public void normalizeObjectiveWeights() {
        double sum = 0;
        for (TargetObjective to : targetList) {
            sum += to.getWeight();
        }
        if (sum == 1) {return;}
        for (TargetObjective to : targetList) {
            to.setWeight(to.getWeight() / sum);
        }
        System.out.println("Normalizing objective weights...");
    }

